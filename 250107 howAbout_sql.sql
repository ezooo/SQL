use awdyy6;

create table aboutMember(
    userName varchar(16) ,
    userId varchar(50) primary key,
    userPw varchar(30),
    userTel char(12),
    userAddr varchar(30),
    nx int,
    ny int,
    userDate char(10),
    userEmail varchar(100) unique,
    enabled boolean,
    iconName text
);

CREATE TABLE email_tokens (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(255) NOT NULL,
    token VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP NOT NULL
);

create table aboutReview(
    userId varchar(50),
    reviewText varchar(500),
    reviewDate char(10),
    millisId bigint primary key,
    placeID varchar(30),
    iconName text,
    foreign key (userId) references aboutMember(userId) on delete cascade
);

create table Place(
    addressName text,
    roadAddress text,
    placeName text,
    category text,
    categoryAll text,
    phone text,
    placeUrl text,
    placeID varchar(30) primary key,
    longitude text,
    latitude text
);

create table aboutWishList(
    userId varchar(50),
    placeId varchar(50),
    placeName text,
    foreign key (userId) references aboutMember(userId) on delete cascade
);

CREATE TABLE addrlocations (
    id INT AUTO_INCREMENT PRIMARY KEY,
    address VARCHAR(255),
    longitude DOUBLE,
    latitude DOUBLE,
    x INT,
    y INT
);

